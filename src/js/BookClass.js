class BookClass {
    constructor(titolo, autore, descrizione, isbn, cover, work, generi, pagine, anno) {
        this.titolo = titolo;
        this.autore = autore;
        this.descrizione = descrizione;
        this.isbn = isbn;
        this.cover = cover;
        this.work = work;
        this.generi = generi;
        this.pagine = pagine;
        this.anno = anno;
    }
}